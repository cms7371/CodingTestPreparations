from collections import deque
INF = int(10e9)
offsets = [(1, 0), (0, 1), (-1, 0), (0, -1)]  # 아래, 오른쪽, 위, 왼쪽


def solution(board):
    n, m = len(board), len(board[0])
    cost = [[INF] * m for _ in range(n)]
    cost[0][0], cost[1][0], cost[0][1] = 0, 100, 100
    orientations = [[[] for _ in range(m)] for _ in range(n)]
    orientations[1][0].append(0)
    orientations[0][1].append(1)
    q = deque()
    if not board[1][0]:
        q.append((1, 0))
    if not board[0][1]:
        q.append((0, 1))
    while q:
        y, x = q.popleft()
        for o in orientations[y][x]:
            for do in [-1, 0, 1]:
                no = (o + do) % 4
                ny, nx = y + offsets[no][0], x + offsets[no][1]
                if 0 <= ny < n and 0 <= nx < m and board[ny][nx] != 1:
                    if do == 0:
                        nc = cost[y][x] + 100
                    else:
                        nc = cost[y][x] + 600
                    if nc < cost[ny][nx]:
                        orientations[ny][nx] = [no]
                        if (ny, nx) not in q:
                            q.append((ny, nx))
                        cost[ny][nx] = nc
                    elif nc == cost[ny][nx]:
                        orientations[ny][nx].append(no)
    return cost[-1][-1]


